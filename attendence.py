class Login:
	def __init__(self,name=None,login_mode=None,login_time=None):
		self.name = name
		self.login_mode = login_mode
		self.login_time = login_time


class Logout:
	def __init__(self,name=None,logout_mode=None,logout_time=None):
		self.name = name
		self.logout_mode = logout_mode
		self.logout_time = logout_time




		